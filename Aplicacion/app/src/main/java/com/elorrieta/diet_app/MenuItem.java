package com.elorrieta.diet_app;

public class MenuItem {
    private String item;

    //Constructor para llenar la lista
    public MenuItem() {

        this.item = item;
    }

    public MenuItem(String item) {

        this.item = item;
    }

    //Get y Set
    public String getItem() {

        return this.item;
    }

    public void setItem(String item) {

        this.item = item;
    }
}
