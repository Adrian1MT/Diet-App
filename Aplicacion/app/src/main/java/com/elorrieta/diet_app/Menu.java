package com.elorrieta.diet_app;

public class Menu {
    private String item;

    //Constructor para llenar la lista
    public Menu() {

        this.item = item;
    }

    public Menu(String item) {

        this.item = item;
    }

    //Get y Set
    public String getItem() {

        return this.item;
    }

    public void setItem(String item) {

        this.item = item;
    }
}
